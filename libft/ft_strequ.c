#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		n;

	n = 0;
	if (s1 && s2)
	{
		n = 1;
		while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
		{
			s1++;
			s2++;
		}
		if (*s1 != *s2)
			n = 0;
	}
	return (n);
}
